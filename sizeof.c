#include <stdio.h>

int main() {
    printf("sizeof(short) é %lu\n", sizeof(short));
    printf("sizeof(unsigned short) é %lu\n", sizeof(unsigned short));
    printf("sizeof(int) é %lu\n", sizeof(int));
    printf("sizeof(unsigned int) é %lu\n", sizeof(unsigned int));
    printf("sizeof(unsigned long) é %lu\n", sizeof(unsigned long));
    printf("sizeof(float) é %lu\n", sizeof(float));
    printf("sizeof(double) é %lu\n", sizeof(double));
    printf("sizeof(long double) é %lu\n", sizeof(long double));
    printf("sizeof(char) é %lu\n", sizeof(char));
    printf("sizeof(char*) é %lu\n", sizeof(char*));

    return 0;
}
